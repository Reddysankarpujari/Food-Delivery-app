function renderHome() {
const grid = document.getElementById('food-grid');
if(!grid){
console.log("food-grid not found");
return;
}
grid.innerHTML = "<p style='color:#9ca3af'>Loading food items...</p>";
  if(!restaurants || restaurants.length === 0){
    grid.innerHTML = "<p style='color:#9ca3af'>No food items found</p>";
    return;
  }

  // collect all menu items from all restaurants
  const allItems = restaurants.flatMap(r =>
    r.menu.map(m => ({
      ...m,
      restaurantName: r.name,
      restaurantId: r.id
    }))
  );


// start with all items
let filtered = allItems;

// apply category filter
if (currentFilter !== "all") {
  filtered = filtered.filter(item => item.category === currentFilter);
}

// apply search filter
if(currentSearch !== ""){

filtered = filtered.filter(item =>
item.name.toLowerCase().includes(currentSearch.toLowerCase()) ||
item.restaurantName.toLowerCase().includes(currentSearch.toLowerCase()) ||
item.category.toLowerCase().includes(currentSearch.toLowerCase())
);

}

// shuffle only when NOT searching
if(currentSearch === ""){
  filtered = filtered
.sort(() => Math.random() - 0.5)
.slice(0,34);
}else{
  // when searching show all results
  filtered = filtered;
}

// show message if nothing found
if (filtered.length === 0) {
  grid.innerHTML = `
    <div style="padding:40px;text-align:center;color:#9ca3af;">
      <i class="fas fa-search" style="font-size:40px;margin-bottom:10px;"></i>
      <div>No food items found</div>
    </div>
  `;
  return;
}

  // render cards
  grid.innerHTML = filtered.map(item => `
    <div class="food-card"
      data-id="${item.id}"
      data-restaurant-id="${item.restaurantId}"
      data-restaurant-name="${item.restaurantName}"
      data-price="${item.price}"
      data-name="${item.name.replace(/"/g,'&quot;')}"
      data-veg="${item.veg}"
      data-category="${item.category}"
      data-image="${item.image}"
    >

      <img class="food-image" src="${item.image}" alt="${item.name}">

      <div class="food-content">
        <div class="food-header">
          <div class="food-name">${item.name}</div>
          <div class="food-price">₹${item.price}</div>
        </div>

        <div class="food-meta">
          <span class="badge ${item.veg ? 'veg':'non-veg'}">
            ${item.veg ? 'Veg':'Non-Veg'}
          </span>

          <span class="restaurant-tag">
            ${item.restaurantName}
          </span>
        </div>
      </div>

    </div>
  `).join('');

  // click add to cart
  grid.querySelectorAll('.food-card').forEach(card => {

    card.addEventListener('click', () => {

      const id = card.dataset.id;
      const restaurantId = card.dataset.restaurantId;
      const restaurantName = card.dataset.restaurantName;
      const price = parseInt(card.dataset.price, 10);
      const name = card.dataset.name;
      const veg = card.dataset.veg === 'true';
      const category = card.dataset.category;
      const image = card.dataset.image;

      addToCart(
        id,
        restaurantId,
        restaurantName,
        price,
        name,
        veg,
        category,
        image,
        false
      );

    });

  });

}
