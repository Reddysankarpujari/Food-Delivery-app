 function updateQty(id,change){
  const item = cart.find(i => i.id === id);
  if(!item) return;
  item.qty += change;
  if(item.qty <= 0){
    cart = cart.filter(i => i.id !== id);
  }
  saveCart();
 updateCartCount();
 updateFloatingCart();
  renderCart();
}
  function removeFromCart(id){
  cart = cart.filter(i => i.id !== id);
  saveCart();
  updateCartCount();
  updateFloatingCart();
  renderCart();
}

async function placeOrder(){

const user = JSON.parse(localStorage.getItem("rkUser"));
const token = localStorage.getItem("rkToken");

if(!user){
alert("Please login before placing order");
openLogin();
return;
}

if(!token){
alert("Authentication expired. Please login again.");
logoutUser();
return;
}

if(!cart.length){
alert("Cart is empty");
return;
}

const subtotal = cart.reduce((s,i)=>s+i.price*i.qty,0);
const delivery = 40;

const order = {
userEmail: user.email,
userName: user.name,
items:[...cart],
subtotal,
delivery,
total: subtotal + delivery,
status:"Placed",
createdAt:new Date(),
deliveryTime:new Date(Date.now()+30*60*1000)
};

try{

const res = await fetch(`${API_URL}/orders`,{
method:"POST",
headers:{
"Content-Type":"application/json",
"Authorization":`Bearer ${token}`
},
body:JSON.stringify(order)
});

const data = await res.json();

if(!res.ok){
throw new Error(data.message || "Order failed");
}

cart=[];
saveCart();
updateCartCount();
updateFloatingCart();
renderCart();

alert("Order placed successfully!");

await loadOrders();
showPage("orders");

}catch(err){

console.error("Order error:",err);
alert("Failed to place order");
}
}

