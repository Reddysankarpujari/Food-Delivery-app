function showSearchSuggestions(term){

const box = document.getElementById("search-suggestions");

if(!box) return;

if(term.length < 2){
box.style.display = "none";
return;
}

const allItems = restaurants.flatMap(r => r.menu);

const matches = allItems
.filter(item => item.name.toLowerCase().includes(term.toLowerCase()))
.slice(0,5);

if(matches.length === 0){
box.style.display = "none";
return;
}

box.innerHTML = matches.map(item => `
<div onclick="selectSuggestion('${item.name}')">
${item.name} <span style="color:#9ca3af;font-size:11px;">(${restaurants.find(r=>r.menu.some(m=>m.id===item.id)).name})</span>
</div>
`).join("");

box.style.display = "block";

}

